int	ft_str_is_printable(char *str)
{
	int	i;
	int	x;

	i = 0;
	x = 1;
	while (str[i] != '\0')
	{
		if (str[i] < 33 || str[i] == 127)
		x = 0;
		i++;
	}
	return (x);
}
